#include<iostream>
using namespace std;

int main()
{
    // 1. Arithmetic operator--> +,-,/,*,%
    int a = 30,b= 20;
    cout <<"Addition of a and b -> "<<(a+b)<<endl;
    cout <<"Subtraction of a and b -> "<<(a-b)<<endl;
    cout <<"Division of a and b -> "<< (a/b)<<endl;
    cout <<"multipication of a and b -> "<<(a*b)<<endl;

    // 2. Relational Operator --> >,>=,<,<=,!=,==
    cout <<(a>b)<<endl;
    cout <<(a>=b)<<endl;
    cout << (a<b)<<endl;
    cout <<(a<=b)<<endl;
    cout <<(a!=b)<<endl;
    cout <<(a==b)<<endl;
    

    return 0;
}